<script setup>
import { urlValidator } from '@core/utils/validators'
import { useItemsStore } from "@/stores/useItemsStore"

import { useAuthStore } from "@/stores/useAuthStore"
const snackbar = ref ({
  enabled: false,
  type: 'success',
  message: 'Hello!',
})
const router = useRouter ()
const itemsStore = useItemsStore ()
const authStore = useAuthStore ()
const tab = ref ('base-info')

const form = ref ({
  name: "",
  description: "",
  state: "draft",
  category: "clothes",
  photos: [ "https://thumbs.dreamstime.com/b/grayscale-random-squares-checkered-pattern-mosaic-abstract-geometric-design-element-cubic-cubism-illustration-214781178.jpg" ],
})

const categoryList = [
  {
    name: 'Clothes',
    value: "clothes",
  },
  {
    name: 'Accessory',
    value: "accessory",
  },
]

const stateList = [
  {
    name: 'Exchnaged',
    value: "exchanged",
  },
  {
    name: 'Exposed',
    value: "exposed",
  },
  {
    name: 'Draft',
    value: "draft",
  },
]

const prepareUrl = title => {
  let t = title.replace ("https://", "").replace ("http://", "")

  return t.slice (0, t.indexOf ('/'))
}

const newItem = async () => {
  snackbar.value = {
    enabled: true,
    message: "Saving item ðŸ§ ",
    type: 'warning',
  }
  itemsStore.postItem (
    {
      user: authStore.$state.userData.id,
      ...form.value,
    },
  ).then (
    response => {
      console.log (response.data)
      if (response.status > 250){
        throw `Failed to save! Response: ${JSON.stringify(response.data)}`
      }
      snackbar.value = {
        enabled: true,
        message: "Item saved ðŸŽ‰",
        type: 'success',
      }
      setTimeout(
        () => {
          router.push ({
            'name': 'items',
          })
        },
        1000,
      )
    },
  ).catch (
    error => {
      snackbar.value = {
        enabled: true,
        message: `Error occured: ${error}`,
        type: 'error',
      }
      console.log (error)
    },
  )
}
</script>

<template>
  <div>
    <VSnackbar
      v-model="snackbar.enabled"
      location="top end"
      variant="flat"
      :color="snackbar.type"
    >
      {{ snackbar.message }}
    </VSnackbar>
    <VTabs v-model="tab">
      <VTab value="base-info">
        Base Info
      </VTab>
      <VTab value="photos">
        Photos
      </VTab>
    </VTabs>
    <VDivider />

    <VCard flat>
      <VCardText>
        <VWindow v-model="tab">
          <VWindowItem value="base-info">
            <VForm class="mt-2">
              <VRow>
                <VCol
                  md="6"
                  cols="12"
                >
                  <VTextField
                    v-model="form.name"
                    label="Name"
                  />
                </VCol>

                <VCol
                  cols="12"
                  md="6"
                >
                  <VSelect
                    v-model="form.category"
                    :items="categoryList"
                    item-value="value"
                    item-title="name"
                    label="Category"
                  />
                </VCol>
                <VCol
                  cols="12"
                  md="6"
                >
                  <VSelect
                    v-model="form.state"
                    :items="stateList"
                    item-value="value"
                    item-title="name"
                    label="State"
                  />
                </VCol>
                <VCol
                  cols="12"
                >
                  <VTextarea
                    v-model="form.description"
                    label="Description"
                    rows="3"
                    auto-grow
                  />
                </VCol>
              </VRow>
            </VForm>
          </VWindowItem>
          <VWindowItem value="photos">
            <VCol
              v-for="(photo, i) in form.photos"
              :key="i"
              md="6"
              cols="12"
            >
              <VTextField
                v-model="form.photos[i]"
                :label="`Photo ${i + 1}`"
                type="text"
                append-inner-icon="tabler-trash"
                :rules="[urlValidator]"
                @click:append-inner="form.photos.splice(i, 1)"
              />
            </VCol>
            <VBtn
              class="justify-end"
              variant="outlined"
              prepend-icon="tabler-plus"
              @click="form.photos.push('')"
            >
              Add photo
            </VBtn>
          </VWindowItem>
        </VWindow>
      </VCardText>

      <VDivider />

      <VCardText class="d-flex gap-4">
        <VBtn
          @click="newItem"
        >
          Submit
        </VBtn>
        <VBtn
          color="secondary"
          variant="tonal"
          :to="{name: 'items'}"
        >
          Cancel
        </VBtn>
      </VCardText>
    </VCard>
  </div>
</template>
